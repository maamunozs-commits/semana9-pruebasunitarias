CREATE TABLE CITAS (
    ID               NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_PACIENTE  VARCHAR2(100)   NOT NULL,
    EMAIL_PACIENTE   VARCHAR2(120)   NOT NULL,
    ESPECIALIDAD     VARCHAR2(100)   NOT NULL,
    NOMBRE_MEDICO    VARCHAR2(100)   NOT NULL,
    FECHA_CITA       DATE            NOT NULL,
    HORA_CITA        VARCHAR2(5)     NOT NULL,
    ESTADO           VARCHAR2(20)    NOT NULL,
    CONSTRAINT PK_CITAS PRIMARY KEY (ID),
    CONSTRAINT CK_CITAS_ESTADO CHECK (ESTADO IN ('PROGRAMADA', 'CONFIRMADA', 'CANCELADA')),
    CONSTRAINT CK_CITAS_HORA CHECK (REGEXP_LIKE(HORA_CITA, '^[0-2][0-9]:[0-5][0-9]$'))
);
