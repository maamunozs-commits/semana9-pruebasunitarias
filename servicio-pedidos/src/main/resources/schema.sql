CREATE TABLE PEDIDOS (
    ID                  NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_PRODUCTO     VARCHAR2(150)   NOT NULL,
    CATEGORIA_PRODUCTO  VARCHAR2(50)    NOT NULL,
    CANTIDAD            NUMBER          NOT NULL,
    PRECIO_UNITARIO     NUMBER(10,2)    NOT NULL,
    NOMBRE_CLIENTE      VARCHAR2(100)   NOT NULL,
    FECHA_PEDIDO        DATE            NOT NULL,
    ESTADO              VARCHAR2(20)    NOT NULL,
    CONSTRAINT PK_PEDIDOS PRIMARY KEY (ID),
    CONSTRAINT CK_PEDIDOS_CANTIDAD CHECK (CANTIDAD > 0),
    CONSTRAINT CK_PEDIDOS_PRECIO CHECK (PRECIO_UNITARIO > 0),
    CONSTRAINT CK_PEDIDOS_ESTADO CHECK (ESTADO IN ('PENDIENTE', 'CONFIRMADO', 'ENVIADO', 'CANCELADO'))
);
